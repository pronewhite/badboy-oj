package com.yupi.badboyOJ.controller;

import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.google.gson.Gson;
import com.yupi.badboyOJ.annotation.AuthCheck;
import com.yupi.badboyOJ.common.BaseResponse;
import com.yupi.badboyOJ.common.DeleteRequest;
import com.yupi.badboyOJ.common.ErrorCode;
import com.yupi.badboyOJ.common.ResultUtils;
import com.yupi.badboyOJ.constant.UserConstant;
import com.yupi.badboyOJ.exception.BusinessException;
import com.yupi.badboyOJ.exception.ThrowUtils;
import com.yupi.badboyOJ.model.dto.question.*;
import com.yupi.badboyOJ.model.dto.questionsubmit.QuestionSubmitAddRequest;
import com.yupi.badboyOJ.model.dto.questionsubmit.QuestionSubmitQueryRequest;
import com.yupi.badboyOJ.model.entity.Question;
import com.yupi.badboyOJ.model.entity.QuestionSubmit;
